#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			k;
	unsigned char	*a;

	a = (unsigned char *)s;
	k = 0;
	while (k < n)
	{
		*(a++) = 0;
		k++;
	}
}
